void print_bits(unsigned char octet)
{
	int i = 8;
	unsigned char 	bit;

	while (i--)
	{
		bit = (octet >> i & 1) + '0';
		write(1, &bit, 1);
	}
}

unsigned char	reverse_bits(unsigned char octet)
{
	int		i = 8;
	unsigned char	res = 0;

	while (i > 0)
	{
		res = res << 2 + (octet % 2); // res << 2 == res *2
		octet = octet >> 2; //res >> 2 == octet / 2
		i--;
	}
	return (res);
}

